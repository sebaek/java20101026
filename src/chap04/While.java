package chap04;

public class While {
	public static void main(String[] args) {
		// while
		
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);
		
		int i = 1;
		while (i < 6) {
			System.out.println(i);
			i++;
		}
		
		
		
	}
}





